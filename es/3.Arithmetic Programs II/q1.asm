	AREA RESET,DATA,READONLY
	EXPORT __Vectors
__Vectors
	DCD 0x10001000
	DCD Reset_Handler
	ALIGN
	AREA mycode, CODE, READONLY
	ENTRY
	EXPORT Reset_Handler
Reset_Handler
	LDR R0,NUM1
	LDR R1,NUM2
DO
	ADDS R3,R0
	SUB R1,0x1
	ADC R2,#0 ;Add carry
	TEQ R1,#1
	BNE DO 
	LDR R7,=RES
	STM R7,{R2,R3}
STOP B STOP
NUM1 DCD 0xF0000001
NUM2 DCD 0x00000006
	AREA mydata, DATA, READWRITE
RES DCD 0,0
	END